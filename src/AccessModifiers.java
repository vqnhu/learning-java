public class AccessModifiers {
    /*
     * no default modifier = hvis du ikke har noe forann en modifier du lager er den ikke tilgjengeig utenfor mappen
     *                       eks.
     * String defaultMessage = "default message";
     * er tilgjengelig i fil a og b som ligger i samme mappe
     * ikke tilgjengelig i fil c som er i en annen mappe
     *
     * public = hvis du har public foran en modifier, er den tilgjengelig i alle klasser selvom den er utenfor mappen.
     *          eks.
     * public String publicMessage = "public message";
     * er tilgjengelig i fil a og b som ligger i samme mappe OG
     * fil c som ligger i en annen mappe
     *
     * protected = hvis du har protected foran en modifier er innholdet tilgjengelig kun hvis en annen klasse arver fra
     * superclass*
     *             eks.
     * protected String protectMessage = "protected message";
     * fil a(superclass/parent) er kun tilgjengelig i fil b eller c dersom de arver fra fil a.
     *
     * private = hvis du har privat foran en modifier er den kun tilgjengelig i klassen
     * */
}
